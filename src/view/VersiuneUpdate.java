    public void displayVersiune() {
        List<Versiune> list = new ArrayList<>();
        
        list = MainController.getInstance().getVersiune();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        Object[] row = new Object[4];
        
        for(int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getVersiuneId();
            row[1] = list.get(i).getNumeVersiune();
            row[2] = list.get(i).getAnInceputFabricatie();
            row[3] = list.get(i).getAnFinalFabricatie();
            
            model.addRow(row);
        }
    }
    
