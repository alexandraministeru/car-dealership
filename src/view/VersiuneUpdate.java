    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        jTextField1.setText(model.getValueAt(i, 1).toString());
        jTextField2.setText(model.getValueAt(i, 2).toString());
        jTextField3.setText(model.getValueAt(i, 3).toString());
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        int i = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel(); 
        
        int versiuneId = Integer.parseInt(model.getValueAt(i, 0).toString());
        String numeVersiune = jTextField1.getText();
        String anInceputFabricatie = jTextField2.getText();
        String anFinalFabricatie = jTextField3.getText();
        
        boolean rez = MainController.getInstance().updateVersiune(versiuneId, numeVersiune, anInceputFabricatie, anFinalFabricatie);
        
        if(rez) {
            
            JOptionPane.showMessageDialog(null, "Versiune actualizata!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            displayVersiune();
        }
    }//GEN-LAST:event_jButton1MouseClicked

   
    

