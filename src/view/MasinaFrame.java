    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        String culoare = jTextField1.getText();
        String tapiterie = jTextField2.getText();
        String kilometraj = jTextField3.getText();
        int capacitatePortbagaj = Integer.parseInt(jTextField5.getText());
        int anFabricatie = Integer.parseInt(jTextField6.getText());
        String pret = jTextField4.getText();
        
        
        Masini masina = new Masini.Builder()
                .setVersiuneMotorId(versiuneMotor.getVersiuneMotorId())
                .setCuloare(culoare)
                .setTapiterie(tapiterie)
                .setKilometraj(kilometraj)
                .setCapacitatePortbagaj(capacitatePortbagaj)
                .setAnFabricatie(anFabricatie)
                .setPret(pret)                
                .build();
        
        boolean rez = MainController.getInstance().adaugaMasina(masina);
        if(rez) {
            JOptionPane.showMessageDialog(null, "Masina adaugata!");
            setVisible(false);
            new AdminFrame().setVisible(true);
        }
                
    }//GEN-LAST:event_jButton1MouseClicked

   

