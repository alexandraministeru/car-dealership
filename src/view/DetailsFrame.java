    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        
        String tractiune = jComboBox1.getSelectedItem().toString();
        String consum = jTextField1.getText();
        String poluare = jTextField2.getText();
        String vitezaMaxima = jTextField3.getText();    
        
        
        VersiuneMotor versiuneMotor = new VersiuneMotor.Builder()
                .setVersiuneId(versiuneId)
                .setMotorId(motorId)
                .setTractiune(tractiune)
                .setConsum(consum)
                .setPoluare(poluare)
                .setVitezamaxima(vitezaMaxima)
                .build();
        
        boolean rez = MainController.getInstance().adaugaVersiuneMotor(versiuneMotor);
        
        if(rez) {
            
            List<VersiuneMotor> list = MainController.getInstance().getVersiuneMotorByVersiuneMotorId(versiuneId, motorId);
            
            if(!list.isEmpty()) {
                new MasinaFrame(list.get(0)).setVisible(true);
                setVisible(false);
            }
            
            
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

    
   

