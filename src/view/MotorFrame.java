    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if (evt.getClickCount() == 2 && evt.getButton() == MouseEvent.BUTTON1 && jList1.getSelectedValue() != null) {
            final Motor motor = jList1.getSelectedValue();
            
            int versiuneId = Integer.parseInt(jLabel2.getText());
            
            new DetailsFrame(versiuneId, motor.getMotorId()).setVisible(true);
            
            setVisible(false);         
            
        }
    }//GEN-LAST:event_jList1MouseClicked

   

